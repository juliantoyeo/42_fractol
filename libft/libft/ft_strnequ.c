#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int diff;

	if (!s1 || !s2)
		return (0);
	diff = ft_strncmp(s1, s2, n);
	if (diff == 0)
		return (1);
	return (0);
}

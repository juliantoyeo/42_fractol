#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		size;

	i = 0;
	size = 0;
	while (s1[size] != '\0')
		size++;
	while (i < n && s2[i] != '\0')
	{
		s1[size] = s2[i];
		i++;
		size++;
	}
	s1[size] = '\0';
	return (s1);
}

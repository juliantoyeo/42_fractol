#include "ft_printf.h"

int		ft_print_colourful(wchar_t *str, int colour)
{
	int		i;

	i = 0;
	while (str[i])
	{
		ft_printf("\033[38;5;%dm%C\033[0m", i + colour, str[i]);
		i++;
	}
	return (0);
}
